package com.CK.Q1.Degiskenler;

public class DegiskenlerTanim {
    public static void main(String[] args) {

        /**
         * Primitive(İlkel) Veri Tipleri
         * 1- byte   ==> 8 bit tamsayı (-128 ile 127 arası)
         * 2- short  ==> 16 bit tamsayı (-32,768 ile 32,767 arası)
         * 3- int    ==> 32 bit tamsayı (-2^31 ile 2^31-1 arası)
         * 4- long   ==> 64 bit tamsayı (-2^63 ile 2^63-1 arası)
         * 5- float  ==> 32 bit noktalı sayı
         * 6- double ==> 64 bit noktalı sayı
         * 7- char   ==> 16 bit Unicode karakter. (0 ile 65,535 arası)
         * 8- boolean => true veya false
         */

        /**
         * Referans Veri Tipleri
         * 1- class    ==> nesneleri tanımlamak için kullanılır
         * 2- interface => arayüzleri tanımlamak için kullanılır, sınıfa özel içi doldurulabilir metodlar barındırır
         * 3- array    ==> dizileri tanımlamak için kullanılır
         */

        /**
         * String Veri Tipi
         * String ==> metin verilerini temsil etmek için kullanılır
         */

        /**
         * Enum Veri Tipi
         * Enum ==> bir türün belirli değerlerini temsil etmek için kullanılır olası karışıklığı önlemek için
         * tercih edilebilir
         */
    }
}
