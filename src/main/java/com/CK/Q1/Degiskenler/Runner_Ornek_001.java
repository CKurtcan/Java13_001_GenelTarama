package com.CK.Q1.Degiskenler;

public class Runner_Ornek_001 {
    public static void main(String[] args) {

        System.out.println("Primitive(İlkel) Veri Tipleri");
        System.out.println();

        byte byteDegiskeni = 121;
        short shortDegiskeni = 32_248;
        int intDegiskeni = 981;
        long longDegiskeni = 1_873L;
        float floatDegiskeni = 3.14f;
        double doubleDegiskeni = 2.1234567;
        char charDegiskeni = 'C';
        boolean booleanDegiskeni = true;

        System.out.println("1-Byte");
        System.out.println("Byte veri tipine örnek, byteDegiskeni = " +byteDegiskeni);

        System.out.println("2-Short");
        System.out.println("Short veri tipine örnek, shortDegiskeni = " +shortDegiskeni);

        System.out.println("3-Int");
        System.out.println("Integer veri tipine örnek, intDegiskeni = " +intDegiskeni);

        System.out.println("4-Long");
        System.out.println("Long veri tipine örnek, longDegiskeni = " +longDegiskeni);

        System.out.println("5-Float");
        System.out.println("Float veri tipine örnek, floatDegiskeni = " +floatDegiskeni);

        System.out.println("6-Double");
        System.out.println("Double veri tipine örnek, doubleDegiskeni = " +doubleDegiskeni);

        System.out.println("7-Char");
        System.out.println("Char veri tipine örnek, charDegiskeni = " +charDegiskeni);

        System.out.println("8-Boolean");
        System.out.println("Boolean veri tipine örnek, booleanDegiskeni = " +booleanDegiskeni);
    }
}
