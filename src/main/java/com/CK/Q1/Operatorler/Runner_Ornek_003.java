package com.CK.Q1.Operatorler;

public class Runner_Ornek_003 {
    public static void main(String[] args) {

        int  x = 11;
        int y = 20;

        System.out.println("Aritmatik Operatörleri Ornekleri");

        System.out.println("1- Ve");
        System.out.println("Ve Kullanımı...: " +x +" > " +y +" && " +y +" == " +y +" koşulları sağlanıyor ise...");

        System.out.println("2- Veya");
        System.out.println("Veya Kullanımı..: " +x +" > " +y +" || " +y +" == " +y +" koşullarından biri sağlanıyor ise...");

        System.out.println("3- Değil");
        System.out.println("Değil Kullanımı...: " +x +" '!=' " +y +" eşit olmadığını ifade eder");

    }
}
