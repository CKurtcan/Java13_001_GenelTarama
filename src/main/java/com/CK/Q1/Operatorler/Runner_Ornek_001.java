package com.CK.Q1.Operatorler;

public class Runner_Ornek_001 {
    public static void main(String[] args) {

        int x = 6;
        int y = 4;

        System.out.println("Aritmatik Operatörleri Ornekleri");

        System.out.println("1- Toplama");
        System.out.println("Toplam sonucu...: " +x +" + " +y +" = " +(x+y));

        System.out.println("2- Çıkarma");
        System.out.println("Fark sonucu...: " +x +" - " +y +" = " +(x-y));

        System.out.println("3- Çarpma");
        System.out.println("Çarpım sonucu...: " +x +" * " +y +" = " +(x*y));

        System.out.println("4- Bölme");
        System.out.println("Bölüm sonucu...: " +x +" / " +y +" = " +((double)x/y));

        System.out.println("5- Mod Alma");
        System.out.println("Mod sonucu...: " +x +" % " +y +" = " +(x%y));

    }
}
