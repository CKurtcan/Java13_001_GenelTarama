package com.CK.Q1.Operatorler;

public class Runner_Ornek_002 {
    public static void main(String[] args) {

        int x = 45;
        int y = 36;

        System.out.println("Karşılaştırma Operatörleri Ornekleri");

        System.out.println("1- Eşit");
        System.out.println("Eşitlik...: " +x +" == " +x);

        System.out.println("2- Eşit Değil");
        System.out.println("Eşit Değillik...: " +x +" != " +y);

        System.out.println("3- Büyük");
        System.out.println("Büyüklük...: " +x +" > " +y);

        System.out.println("4- Küçük");
        System.out.println("Küçüklük...: " +y +" < " +x);

        System.out.println("5- Büyük Eşit");
        System.out.println("Büyük Eşitlik...: " +x +" >= " +y);

        System.out.println("5- Küçük Eşit");
        System.out.println("Küçük Eşitlik...: " +y +" <= " +y);



    }
}
