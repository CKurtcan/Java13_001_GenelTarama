package com.CK.Q1.Operatorler;

public class Runner_Ornek_004 {
    public static void main(String[] args) {

        int x = 6;
        int y = 4;

        System.out.println("Aritmatik Operatörleri Ornekleri");

        System.out.println("1- Arttırma");
        System.out.println("Arttırma...:  ilk durum x = " +x +" ++x işlem sonrası = " +(++x));

        System.out.println("2- Azaltma");
        System.out.println("Azaltma...:  ilk durum y = " +y +" --y işlem sonrası = " +(--y));

        System.out.println("3- Sonra Arttırma");
        System.out.println("Sonra Arttırma...: ilk durum x = " +x +" x++ + ++x " +"işlem sonrası = " +(x+2));

        System.out.println("4- Sonra Azaltma");
        System.out.println("Sonra Arttırma...: ilk durum y = " +y +" y-- + --y " +"işlem sonrası = " +(y-2));


    }
}
