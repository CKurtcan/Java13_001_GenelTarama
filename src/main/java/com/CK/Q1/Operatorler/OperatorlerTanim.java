package com.CK.Q1.Operatorler;

public class OperatorlerTanim {

        /**
         * Aritmatik Operatörleri
         * 1- Toplama ==> '+'
         * 2- Çıkarma ==> '-'
         * 3- Çarpma  ==> '*'
         * 4- Bölme   ==> '/'
         * 5- Mod Alma => '%'
         */

        /**
         * Karşılaştırma Operatörleri
         * 1- Eşit      ==> '=='
         * 2- Eşit değil => '!='
         * 3- Büyük     ==> '>'
         * 4- Küçük     ==> '<'
         * 5- Büyük eşit => '>='
         * 6- Küçük eşit => '<='
         */

        /**
         * Mantıksal Operatörler
         * 1- Ve    ==> '&&'
         * 2- Veya  ==> '||'
         * 3- Değil ==> '!'
         */

        /**
         * Arttırma Azaltma Operatörleri
         * 1- Bir arttırma ==> '++'
         * 2- Bir azaltma  ==> '--'
         */

        /**
        *Atama Operatörleri
         * 1- Atama            ==> '='
         * 2- Toplayarak Atama ==> '+='
         * 3- Çıkartarak Atama ==> '-='
         * 4- Çarparak Atama   ==> '*='
         * 5- Bölerek Atama    ==> '/='
         * 6- Mod Alarak Atama ==> '%='
        */

}
